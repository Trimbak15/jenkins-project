public class Main {
    // The main method: the starting point of the program
    public static void main(String[] args) {
        // Variables and Data Types
        int age = 20;               // Integer
        String message = "Java is "; // String
        
        // Simple Conditional (If-Else) Statement
        if (age >= 18) {
            System.out.println(message + "fun and I am an adult.");
        } else {
            System.out.println(message + "fun and I am a minor.");
        }
        
        // Loop Example: Printing 1 to 3
        for (int i = 1; i <= 3; i++) {
            System.out.println("Loop iteration: " + i);
        }
    }
}
